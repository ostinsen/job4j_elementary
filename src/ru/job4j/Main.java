package ru.job4j;

public class Main {
    public static void main(String[] args) {
        System.out.println("Hello world");
        long l = 129;
        byte b = (byte) l;
        System.out.println(b);

        float f = 12358.7f;
        int i = 45981;
        short rsl = (short) (f + i);
        System.out.println(rsl);

        char c = 45000;
        float ii = c;
        System.out.println(ii);

        double d = 121.19;
        byte bb = (byte) d;
        System.out.println(bb);

        short s = 1500;
        char cc = (char) s;
        double dd = cc;
        System.out.println(dd);
    }
}